# For loop in list
thislist=["apple","banana","cherry"]
for x in thislist:
  print(x)


#   output
#   apple
#   banana
#   cherry

# while loop in list
thislist = ["apple", "banana", "cherry"]
i=0
while i<len(thislist):
  print(thislist[i])
  i=i+1


#   output
#   apple
#   banana
#   cherry
