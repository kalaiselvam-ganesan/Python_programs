# casting
 
x=str(3)        # x is string type having value 3  
print(x)
print(float(x)) # converting string to float value
print(int(x))   # onerting string value to integer value

y=float(2)      # y is float type having value 2
print(y)
print(int(y))   # converting float into integer value
print(str(y))   # converting float into string value

z=int(12)       # z is integer type having value 12
print(z)       
print(float(z)) # converting integer into float value
print(str(z))   # converting integer into string value