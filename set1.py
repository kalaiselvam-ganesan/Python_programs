# Set

# set is created and it is unordered
thisset={"kalai","selvam",23.0,45}
print(thisset)

# set does not allow duplicate elements
thisset={"kalai","selvam",23.0,45,"kalai"}
print(thisset)

# len() method is used to define the length
thisset={"kalai","selvam",23.0,45,"kalai"}
print(len(thisset))

# type() method used to define the types
thisset={"kalai","selvam",23.0,45}
print(type(thisset))

# it allows all the datatype in set like innt,str,bool..etc
thisset={"kalai","selvam",23.0,45,True}
print(thisset)


#   output

#   {45, 'kalai', 23.0, 'selvam'}
#   {45, 'kalai', 23.0, 'selvam'}
#   4
#   <class 'set'>
#   {True, 45, 'kalai', 23.0, 'selvam'}
 