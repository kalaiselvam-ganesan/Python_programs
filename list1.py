# list

# list can be created using square brackets
thislist=["apple",20,'kalai',40.6]
print(thislist)

# allow dupllicate values in list
thislist=["apple",20,'kalai',40.6,"kalai"]
print(thislist)

#length of the list using len() function
thislist=["apple",20,'kalai',40.6]
print(len(thislist))

# containing all datatypes like integer,string,boolean,float
thislist=["apple",20,'kalai',40.6,True]
print(thislist)

# checking the class type using type() function 
thislist=["apple",20,'kalai',40.6,True]
print(type(thislist))


