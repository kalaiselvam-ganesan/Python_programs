# function

# function definition
def my_function(f_name):
    print("Hi," + f_name +" " + "Welcome to my world!")

# function call
my_function("kalai")
# argument passing to the function
my_function("ram")
my_function("sam")


#   output

#   Hi,kalai Welcome to my world! 
#   Hi,ram Welcome to my world! 
#   Hi,sam Welcome to my world! 