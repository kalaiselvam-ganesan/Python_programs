# datatypes and its tpyes..

# complex
x=1j
print(type(x))
# integer
y=20
print(type(y))
# string
z='klll'
print(type(z))
# frozenset
f=frozenset({10,20,30})
print(type(f))
# range
e=range(6)
print(type(e))
# set
d={10,20,30}
print(type(d))
# tuple
c=(10,20,30)
print(type(c))
# list
b=[10,20,30]
print(type(b))
# float
a=20.5
print(type(a))
# None
g=None
print(type(g))

#   output
#   <class 'complex'>
#   <class 'int'>
#   <class 'str'>
#   <class 'frozenset'>
#   <class 'range'>
#   <class 'set'>
#   <class 'tuple'>
#   <class 'list'>
#   <class 'float'>
#   <class 'NoneType'>
